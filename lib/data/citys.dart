class Citys {
  String city;
  String region;
  String flag;

  Citys(String city, String region, String flag) {
    this.city = city;
    this.region = region;
    this.flag = flag;
  }

  String getCity() {
    return city;
  }

  String getRegion() {
    return region;
  }

  String getFlag() {
    return flag;
  }
}
